#include "minishell.h"

void ft_echo(t_cmd *cmd)
{
    int i;

    i = 0;
    while(cmd->cmd_args[++i])
    {
        ft_putstr_fd(cmd->cmd_args[i], 1);
        write(1, " ", 1);
    }
    write(1, "\n", 1);
}