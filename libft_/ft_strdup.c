#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	i;
	char	*arr;

	i = 0;
	arr = malloc(ft_strlen(s1) + 1);
	if (!arr)
		return (0);
	while (s1[i])
	{
		arr[i] = s1[i];
		i++;
	}
	arr[i] = '\0';
	return (arr);
}
